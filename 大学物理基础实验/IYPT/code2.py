import curve
import numpy as np
import warnings
warnings.filterwarnings("ignore")
rho0=1.757469244288225e-07
alpha=0.0069
def compute_T(R,R0,T0,r):
    S=3.1415925*r**2
    L=R0*S/(rho0*(1+alpha*T0))
    T=(R*S/L/rho0-1)/alpha
    return T

